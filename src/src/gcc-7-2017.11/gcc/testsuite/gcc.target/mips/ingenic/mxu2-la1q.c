/* Test builtins for MIPS MXU2 ASE instructions */
/* { dg-options "-mmxu2" } */

#include "mxu2.h"
#include "mxu2-check.h"

int main (void)
{
  union_signed_b u1, u2;
  union_signed_b v1, v2;
  int i;
  int len = 16;
  char value1[32] = {0xA,0x44,0x9,0x33,0x8,0x22,0x7,0x11,0x6,0x10,0xA,0x55,0xA,0x55,0xA,0x55,0xA,0x55,0xA,0x55,0xA,0x55,0xA,0x55,0xA,0x55,0xA,0x55,0xA,0x55,0xA,0x55,};
  char *v;
  v = malloc(sizeof(char) * 80);

  v = (char *)((unsigned int)v + 0x10);
  v = (char *)((unsigned int)v & (~(0xF)));

  for(i = 0; i < 32; i++)
  {
    *(v+i) = value1[i];
  }
  u1.x  = __builtin_mxu2_la1q(v, 0);
  u2.x  = __builtin_mxu2_la1q(v, 16);

  v1.x = (v16i8){(0xA), (0x44), (0x9), (0x33), (0x8), (0x22), (0x7), (0x11), (0x6) ,(0x10), (0xA), (0x55), (0xA), (0x55), (0xA), (0x55),};
  v2.x = (v16i8){(0xA), (0x55), (0xA), (0x55), (0xA), (0x55), (0xA), (0x55), (0xA), (0x55), (0xA), (0x55), (0xA), (0x55), (0xA), (0x55),};


  if(check_b (&u1, &v1, len))
     abort ();
  if(check_b (&u2, &v2, len))
     abort ();
  return 0;
}
